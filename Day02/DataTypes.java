public class DataTypes {
    public static void main(String[] args) {
        
        System.out.println("These are the primitive data types:");
        System.out.println();
        System.out.println("1. byte | Byte Integer");
        System.out.println("2. short | Short Integer");
        System.out.println("3. int | Integer");
        System.out.println("4. long | Long Integer");
        System.out.println("5. float | Real Number");
        System.out.println("6. double | Long Real Number");
        System.out.println("7. boolean | Boolean (true or false)");
        System.out.println("8. char | Character");

        System.out.println();
        System.out.println("These are the comment non-primitive data types:");
        System.out.println();
        System.out.println("1. String | Collection of characters");
        System.out.println("2. Array | Collection of values of the same data type");
    }
}