public class DataTypes {
    public static void main(String[] args) {
        
        System.out.println("These are the primitive data types:");
        System.out.println();
        System.out.println("1. int | Integer");
        System.out.println("2. long | Long Integer");
        System.out.println("3. float | Real Number");
        System.out.println("4. double | Long Real Number");
        System.out.println("5. boolean | Boolean (true or false)");
        System.out.println("6. char | Character");

        System.out.println();
        System.out.println("These are the comment non-primitive data types:");
        System.out.println();
        System.out.println("1. String | Collection of characters");
        System.out.println("2. Array | Collection of values of the same data type");
    }
}