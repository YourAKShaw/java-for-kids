// Relational Operators

// Relational Operators are used for finding 
// the relationship between two values on the 
// basis of their values. The return type for a 
// relational operation is boolean, i.e., it returns 
// true or false.

// Realation Operators in Java

// <	-- less than
// >	-- greater than
// <=	-- less than or equal to
// >=	-- greater than or equal to
// ==	-- equality
// !=	-- not equal

public class RelationalOperators {
    public static void main(String[] args) {

        System.out.println("5 < 6 is " + (5 < 6));
        System.out.println("5 > 6 is " + (5 > 6));
        System.out.println("5 <= 6 is " + (5 <= 6));
        System.out.println("5 >= 6 is " + (5 >= 6));
        System.out.println("5 == 6 is " + (5 == 6));
        System.out.println("5 != 6 is " + (5 != 6));
        System.out.println();
        System.out.println("Distinction between < and <= :");
        System.out.println("6 < 6 is " + (6 < 6));
        System.out.println("6 <= 6 is " + (6 <= 6));
    }
}